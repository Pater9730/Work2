public abstract class Spaceship  {

    /**
     * Atributos de cada nave
     */

    public int Speed;
    public int Weight;
    public float Height;

    /**
     * Primer comportamiento abstracto de cada nave
     */

    public abstract void Propulsion();

    /**
     * Segundo comportamiento abstracto
     */

    public abstract void Performingmissions ();







}
